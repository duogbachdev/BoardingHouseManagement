    private void txtGantepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtGantepActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setMultiSelectionEnabled(true); // Cho phép chọn nhiều file
        chooser.showOpenDialog(null);
        File[] files = chooser.getSelectedFiles(); // Lấy danh sách các file được chọn
        StringBuilder sb = new StringBuilder();
        for (File file : files) {
            String path = file.getAbsolutePath();
            sb.append(path).append("; "); // Xây dựng danh sách đường dẫn
        }
        String filePaths = sb.toString();
        if (!filePaths.isEmpty()) {
            filePaths = filePaths.substring(0, filePaths.length() - 2); // Loại bỏ dấu ; cuối cùng
        }
        txtTep.setText(filePaths);
    }//GEN-LAST:event_txtGantepActionPerformed

    private void btnGuiMailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuiMailActionPerformed
        try {
            Properties p = new Properties();
            p.put("mail.smtp.auth", "true");
            p.put("mail.smtp.starttls.enable", "true");
            p.put("mail.smtp.host", "smtp.gmail.com");
            p.put("mail.smtp.port", "587");
            String nguoigui = txtNguoiGui.getText();
            String pass = txtPass.getText();
            Session s = Session.getInstance(p,
                    new Authenticator() {
                @Override
                protected PasswordAuthentication getPasswordAuthentication() {
                    return new PasswordAuthentication(nguoigui, pass);
                }
            });
            String from = txtNguoiGui.getText();
            String nguoinhan = txtNguoinhan.getText();
            String tieude = txtTieude.getText();
            String noidung = txtND.getText();
            String tepdinhkem = txtTep.getText();
            Message msg = new MimeMessage(s);
            msg.setFrom(new InternetAddress(from));
            msg.setRecipients(Message.RecipientType.TO, InternetAddress.parse(nguoinhan));

            // Tạo phần nội dung email
            MimeBodyPart contentPart = new MimeBodyPart();
            contentPart.setContent(noidung, "text/html; charset=utf-8");

            // Tạo phần đính kèm
            MimeBodyPart filePart = new MimeBodyPart();
            FileDataSource fileDataSource = new FileDataSource(tepdinhkem);
            filePart.setDataHandler(new DataHandler(fileDataSource));
            filePart.setFileName(fileDataSource.getName());

            // Tạo đối tượng Multipart và thêm các phần vào
            Multipart multipart = new MimeMultipart();
            multipart.addBodyPart(contentPart);
            multipart.addBodyPart(filePart);

            // Đặt tiêu đề và nội dung cho email
            msg.setSubject(tieude);
            msg.setContent(multipart);

            // Gửi email
            Transport.send(msg);
            JOptionPane.showMessageDialog(null, "Gửi thành công");
        } catch (Exception e) {
            // Handle exceptions appropriately
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuiMailActionPerformed

